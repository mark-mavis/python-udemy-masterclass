
#String is a python "Sequence type"


parrot = "Norwegian Blue"
print(parrot)

#print index 3
print(parrot[3])

#Negative Indexing in strings
print(parrot[-1])   #prints the last element in the string
print(parrot[-2])   #prints the second to last element in the string
print(parrot[13-14])    #prints the last element in the string
print(parrot[12-14])    #prints the second to last element in the string
print(parrot[11-14])    #prints the third to last element in the string
print(parrot[10-14])    #prints the fourth to last element in the string
